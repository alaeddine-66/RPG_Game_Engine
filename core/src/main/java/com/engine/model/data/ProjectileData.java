package com.engine.model.data;


public class ProjectileData {
    private String type;
    private String path;
    private int width;
    private int height;
    private int speed;

    // Getters et setters

    public String getType() {
        return type;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getSpeed() {
        return speed;
    }

    public String getPath() {
        return path;
    }
}
